#Primeiro, é necessário entender qual a relação entre segundos e horas. Temos que 1 hora é igual a 60 minutos,
# e cada minuto é igual a 60 segundos. Logo:
# s=h*(60*60)

#'1': Para uma hora e meia, h=1.5
h=1.5
s=h*(60*60)
print('uma hora e meia é igual a', s, 'segundos')
s=5400.00
#Resposta exercício '1': Para h=1.5, S=5400 segundos.

#'2': para 12 horas, temos que:
h=12
s=h*60*60
